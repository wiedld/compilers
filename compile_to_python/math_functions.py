def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def division(n1, n2):
    return n1 / n2

def power(n1, n2):
    return n1 ** n2

def mod(n1, n2):
    return n1 % n2

def factorial(n):
    if n == 1:
        return n
    return n * factorial(n-1)

